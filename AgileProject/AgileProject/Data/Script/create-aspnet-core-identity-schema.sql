CREATE PROCEDURE [dbo].[sp_validar_usuario]
    @UserName VARCHAR(100),
    @Clave VARCHAR(100)
AS
BEGIN
    SELECT top 1 a.Id, ISNULL(a.Email,''),ISNULL(a.PhoneNumber,''),a.UserName,ISNULL(a.Nombres,''),ISNULL(a.ApellidoPaterno,''),ISNULL(a.ApellidoMaterno,''),ISNULL(a.FechaNacimiento, '1900-01-01'),c.Name as Role
    FROM AspNetUsers a
    LEFT JOIN aspNetUserRoles b on a.id = b.Userid
    LEFT JOIN aspNetRoles c on c.id = b.RoleId
    WHERE a.UserName=@UserName and a.PasswordHash=@Clave
    order by c.Name Asc
END
GO


