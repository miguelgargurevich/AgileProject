create PROCEDURE [dbo].[sp_change_password]
    @UserName VARCHAR(100),
    @Clave VARCHAR(100)
AS
BEGIN
    UPDATE AspNetUsers set PasswordHash=@Clave
    WHERE UserName=@UserName 
END
